SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

DROP TABLE IF EXISTS `tb_wait_time_report`;
DROP TABLE IF EXISTS `tb_crowd_report`;
DROP TABLE IF EXISTS `tb_dish_review`;
DROP TABLE IF EXISTS `tb_food_order_item`;
DROP TABLE IF EXISTS `tb_food_order`;
DROP TABLE IF EXISTS `tb_canteen_recommend`;
DROP TABLE IF EXISTS `tb_dish`;
DROP TABLE IF EXISTS `tb_window`;
DROP TABLE IF EXISTS `tb_canteen`;
DROP TABLE IF EXISTS `tb_user_info`;
DROP TABLE IF EXISTS `tb_user`;

CREATE TABLE `tb_user` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT 'primary key',
  `phone` varchar(11) NOT NULL COMMENT 'phone number',
  `password` varchar(128) DEFAULT '' COMMENT 'password hash, empty means sms-login only',
  `role` tinyint(1) UNSIGNED NOT NULL DEFAULT 0 COMMENT '0 user,1 admin',
  `nick_name` varchar(32) DEFAULT '' COMMENT 'nickname',
  `icon` varchar(255) DEFAULT '' COMMENT 'avatar',
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_phone` (`phone`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_user_info` (
  `user_id` bigint(20) UNSIGNED NOT NULL,
  `city` varchar(64) DEFAULT NULL COMMENT 'user editable',
  `introduce` varchar(256) DEFAULT NULL COMMENT 'user editable',
  `gender` tinyint(1) DEFAULT NULL COMMENT 'user editable',
  `birthday` date DEFAULT NULL COMMENT 'user editable',
  `credits` int(10) UNSIGNED DEFAULT 0 COMMENT 'system field',
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_canteen` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `name` varchar(128) NOT NULL,
  `address` varchar(255) NOT NULL,
  `images` varchar(1024) DEFAULT NULL,
  `x` double UNSIGNED NOT NULL,
  `y` double UNSIGNED NOT NULL,
  `crowd_level` tinyint(1) UNSIGNED NOT NULL DEFAULT 0 COMMENT '0 free,1 normal,2 crowded',
  `crowd_score` int(3) UNSIGNED NOT NULL DEFAULT 0 COMMENT '0~100',
  `open_hours` varchar(255) DEFAULT NULL,
  `status` tinyint(1) UNSIGNED NOT NULL DEFAULT 1 COMMENT '1 open,0 closed',
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_window` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `canteen_id` bigint(20) UNSIGNED NOT NULL,
  `name` varchar(128) NOT NULL,
  `description` varchar(512) DEFAULT NULL,
  `wait_time` int(3) UNSIGNED NOT NULL DEFAULT 0,
  `wait_time_level` tinyint(1) UNSIGNED NOT NULL DEFAULT 0 COMMENT '0<5,1=5~10,2>10',
  `status` tinyint(1) UNSIGNED NOT NULL DEFAULT 1 COMMENT '1 on sale,0 off sale',
  `sort` int(3) UNSIGNED NOT NULL DEFAULT 0 COMMENT 'display order in one canteen, smaller first',
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_window_canteen` (`canteen_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_dish` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `window_id` bigint(20) UNSIGNED NOT NULL,
  `name` varchar(128) NOT NULL,
  `description` varchar(512) DEFAULT NULL,
  `image` varchar(255) DEFAULT NULL,
  `price` decimal(10,2) UNSIGNED NOT NULL,
  `quantity` int(10) UNSIGNED NOT NULL DEFAULT 0 COMMENT 'available quantity',
  `tags` varchar(255) DEFAULT NULL,
  `avg_rating` decimal(3,1) UNSIGNED NOT NULL DEFAULT 0.0,
  `rating_count` int(10) UNSIGNED NOT NULL DEFAULT 0,
  `status` tinyint(1) UNSIGNED NOT NULL DEFAULT 1 COMMENT '1 on sale,0 off sale',
  `is_today` tinyint(1) UNSIGNED NOT NULL DEFAULT 1,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_dish_window` (`window_id`),
  KEY `idx_dish_today` (`is_today`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_food_order` (
  `id` bigint(20) UNSIGNED NOT NULL COMMENT 'order id generated by redis id worker',
  `user_id` bigint(20) UNSIGNED NOT NULL,
  `order_no` varchar(32) NOT NULL,
  `total_amount` decimal(10,2) UNSIGNED NOT NULL DEFAULT 0.00,
  `total_count` int(10) UNSIGNED NOT NULL DEFAULT 0,
  `status` tinyint(1) UNSIGNED NOT NULL DEFAULT 0 COMMENT '0 created,1 accepted,2 ready,3 completed,4 cancelled',
  `pickup_code` varchar(12) DEFAULT NULL COMMENT 'code for meal pickup',
  `remark` varchar(255) DEFAULT NULL,
  `cancel_reason` varchar(255) DEFAULT NULL,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_order_no` (`order_no`),
  KEY `idx_order_user_time` (`user_id`,`create_time`),
  KEY `idx_order_status_time` (`status`,`create_time`),
  CONSTRAINT `fk_food_order_user` FOREIGN KEY (`user_id`) REFERENCES `tb_user` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_food_order_item` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `order_id` bigint(20) UNSIGNED NOT NULL,
  `dish_id` bigint(20) UNSIGNED NOT NULL,
  `dish_name` varchar(128) NOT NULL,
  `dish_image` varchar(255) DEFAULT NULL,
  `window_id` bigint(20) UNSIGNED NOT NULL,
  `price` decimal(10,2) UNSIGNED NOT NULL,
  `quantity` int(10) UNSIGNED NOT NULL DEFAULT 1,
  `amount` decimal(10,2) UNSIGNED NOT NULL DEFAULT 0.00,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_order_item_order` (`order_id`),
  KEY `idx_order_item_dish` (`dish_id`),
  CONSTRAINT `fk_order_item_order` FOREIGN KEY (`order_id`) REFERENCES `tb_food_order` (`id`) ON DELETE CASCADE,
  CONSTRAINT `fk_order_item_dish` FOREIGN KEY (`dish_id`) REFERENCES `tb_dish` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_dish_review` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `dish_id` bigint(20) UNSIGNED NOT NULL,
  `user_id` bigint(20) UNSIGNED NOT NULL,
  `rating` tinyint(1) UNSIGNED NOT NULL,
  `content` varchar(512) DEFAULT NULL,
  `tags` varchar(255) DEFAULT NULL,
  `images` varchar(1024) DEFAULT NULL,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_review_dish` (`dish_id`),
  KEY `idx_review_user` (`user_id`),
  UNIQUE KEY `uk_review_user_dish` (`user_id`,`dish_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_canteen_recommend` (
  `canteen_id` bigint(20) UNSIGNED NOT NULL,
  `dish_id1` bigint(20) UNSIGNED NOT NULL,
  `dish_id2` bigint(20) UNSIGNED NOT NULL,
  `dish_id3` bigint(20) UNSIGNED NOT NULL,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`canteen_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_crowd_report` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `canteen_id` bigint(20) UNSIGNED NOT NULL,
  `user_id` bigint(20) UNSIGNED NOT NULL,
  `crowd_level` tinyint(1) UNSIGNED NOT NULL,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_crowd_canteen` (`canteen_id`),
  KEY `idx_crowd_time` (`create_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `tb_wait_time_report` (
  `id` bigint(20) UNSIGNED NOT NULL AUTO_INCREMENT,
  `window_id` bigint(20) UNSIGNED NOT NULL,
  `user_id` bigint(20) UNSIGNED NOT NULL,
  `wait_time` int(3) UNSIGNED NOT NULL,
  `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `idx_wait_window` (`window_id`),
  KEY `idx_wait_time` (`create_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

